def solution(wallpaper):
  on, off = [],[]
  for row in range(len(wallpaper)):
    for col in range(len(wallpaper[row])):
      if wallpaper[row][col] == '#':
        on.append(row)
        off.append(col)

  return [min(row), min(col), max(row)+1, max(col)+1]

